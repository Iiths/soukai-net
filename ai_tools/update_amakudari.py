def make_id(name: str) -> str:
    safe = re.sub(r'[^\w]', '_', name)[:20]
    uid = str(uuid.uuid4()).split('-')[0]
    return f"{safe}_{uid}"


def has_org(ninja: dict, org_id: str) -> bool:
    return any(o.get("id") == org_id for o in (ninja.get("organizations") or []))


def add_org(ninja: dict, org: dict):
    if ninja.get("organizations") is None:
        ninja["organizations"] = []
    if not has_org(ninja, org["id"]):
        ninja["organizations"].append({"id": org["id"], "name": org["name"]})


def build_name_index(ninjas: list) -> dict:
    idx: dict = {}
    for n in ninjas:
        idx[n["name"]] = n
        for alias in (n.get("aliases") or []):
            idx[alias] = n
    return idx


def process(members: list, org: dict, ninjas: list, index: dict):
    updated = 0
    added = 0
    for (name, aliases, ninja_type) in members:
        if name in index:
            ninja = index[name]
            add_org(ninja, org)
            if ninja_type and not ninja.get("ninjaType"):
                ninja["ninjaType"] = ninja_type
            if aliases:
                existing = set(ninja.get("aliases") or [])
                for a in aliases:
                    if a not in existing:
                        ninja.setdefault("aliases", []).append(a)
                        index[a] = ninja
            updated += 1
        else:
            new_ninja: dict = {
                "id": make_id(name),
                "name": name,
                "organizations": [{"id": org["id"], "name": org["name"]}],
                "appearances": [],
            }
            if aliases:
                new_ninja["aliases"] = aliases
            if ninja_type:
                new_ninja["ninjaType"] = ninja_type
            ninjas.append(new_ninja)
            index[name] = new_ninja
            if aliases:
                for a in aliases:
                    index[a] = new_ninja
            added += 1
    return updated, added


